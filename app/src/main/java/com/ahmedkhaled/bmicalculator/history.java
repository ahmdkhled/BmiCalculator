public class history {

    public int id;
    public String weight;
    public String height;
    public String age;
    public String result;
    public String date;

    public history(int id, String weight, String height, String age, String result, String date) {
        this.id = id;
        this.weight = weight;
        this.height = height;
        this.age = age;
        this.result = result;
        this.date = date;
    }
}
